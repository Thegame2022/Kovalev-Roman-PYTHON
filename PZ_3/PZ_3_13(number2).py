# Дан номер года (положительное целое число). Определить количество дней в этом
# году, учитывая, что обычный год насчитывает 365 дней, а високосный — 366 дней.
# Високосным считается год, делящийся на 4, за исключением тех годов, которые
# делятся на 100 и не делятся на 400 (например, годы 300, 1300 и 1900 не являются
# високосными, а 1200 и 2000 — являются).
try:
    # Ввод номера года
    year = int(input("Введите номер года (положительное целое число): "))    
    # Проверка, является ли год високосным
    if year <= 0:
        print("Ошибка: год должен быть положительным целым числом.")
    else:
        if (year % 4 == 0 and year % 100 != 0) or (year % 400 == 0):
            print(f"Год {year} является високосным. В этом году 366 дней.")
        else:
            print(f"Год {year} является обычным. В этом году 365 дней.")
except ValueError:
    print("Ошибка: введено некорректное значение. Пожалуйста, введите положительное целое число.")
