# 1. Даны три целых числа: A, B, C. Проверить истинность высказывания: «Хотя бы одно
# из чисел A, B, C положительное»
try:
    # Ввод трех целых чисел
    A = int(input("Введите число A: "))
    B = int(input("Введите число B: "))
    C = int(input("Введите число C: "))
    
    # Проверка, хотя бы одно из чисел положительное
    if A > 0 or B > 0 or C > 0:
        print("Истинно: хотя бы одно из чисел A, B, C положительное.")
    else:
        print("Ложно: ни одно из чисел A, B, C не положительное.")
except ValueError:
    print("Ошибка: введено некорректное значение. Пожалуйста, введите целые числа.")
