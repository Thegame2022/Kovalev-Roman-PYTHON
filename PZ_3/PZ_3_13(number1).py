# 1. Даны три целых числа: A, B, C. Проверить истинность высказывания: «Хотя бы одно
# из чисел A, B, C положительное»
while True:
    try:
        # Ввод трех целых чисел
        user_input = input("Введите три целых числа A, B, C через пробел (или 'exit' для выхода): ")
        # Проверка на выход
        if user_input.lower() == 'exit':
            print("Выход из программы.")
            break   
        # Преобразование входных данных в список целых чисел
        A, B, C = map(int, user_input.split())
        # Проверка, хотя бы одно число положительное
        if A > 0 or B > 0 or C > 0:
            print("Хотя бы одно из чисел A, B, C положительное.")
        else:
            print("Все числа A, B, C не положительные.")
    except ValueError:
        print("Ошибка: введено некорректное значение. Пожалуйста, введите три целых числа через пробел.")
